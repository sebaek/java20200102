package chapter03;

public class Comparison {
	public static void main(String[] args) {
		// == != > < >= <=
		// 결과는 boolean 타입
		
		System.out.println(3 == 4);
		System.out.println(3 == 3);
		
		System.out.println(3 != 5);
		System.out.println(3 != 3);
		
		System.out.println(3 > 5);
		System.out.println(5 > 3);
		
		System.out.println(3 < 5);
		System.out.println(5 < 3);
		
		System.out.println(3 <= 5);
		System.out.println(5 <= 3);
		
		System.out.println(3 >= 5);
		System.out.println(5 >= 3);
		
	}
}









